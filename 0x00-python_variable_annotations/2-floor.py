#!/usr/bin/env python3
"""
This module provides a function to find the floor of a float.
"""

import math


def floor(n: float) -> int:
    """
    Returns the floor of the given float.

    Args:
        n (float): The float value to find the floor of.

    Returns:
        int: The floor of the input float.
    """
    return math.floor(n)
